#include <string.h>
#include "libft.h"

char	*ft_strmap(char const *s, char (*f)(char))
{
	char	*newstr;
	size_t	len;

	len = ft_strlen(s);
	newstr = ft_strnew(len);
	if (newstr)
	{
		while (len > 0)
		{
			newstr[len - 1] = f(s[len - 1]);
			len--;
		}
	}
	return (newstr);
}
