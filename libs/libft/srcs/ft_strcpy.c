#include <string.h>

char	*ft_strcpy(char *dest, const char *src)
{
	char *dst;

	if (src == NULL)
		return (dest);
	dst = dest;
	while (*src)
		*dst++ = *src++;
	*dst = 0;
	return (dest);
}
