#include <stdlib.h>
#include "libft.h"

char	*ft_strdup(const char *src)
{
	size_t	len;
	char	*copy;

	if (src == NULL)
		return (NULL);
	len = ft_strlen(src);
	copy = ft_strnew(len);
	ft_memcpy((void *)copy, (const void *)src, len);
	return (copy);
}
