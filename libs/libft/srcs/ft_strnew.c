#include <stdlib.h>

char	*ft_strnew(size_t size)
{
	char *str;

	size++;
	str = (char *)malloc(size);
	if (str == NULL)
		return (NULL);
	while (size != 0)
	{
		str[size - 1] = 0;
		size--;
	}
	return (str);
}
