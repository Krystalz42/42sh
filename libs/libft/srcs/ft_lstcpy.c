#include "libft.h"
#include <stdlib.h>

t_list	*ft_lstcpy(t_list *list)
{
	t_list *cpy;

	if (list != NULL)
	{
		cpy = ft_lstnew(list->content, list->content_size);
		cpy->next = ft_lstcpy(list->next);
		return (cpy);
	}
	else
		return (NULL);
}
