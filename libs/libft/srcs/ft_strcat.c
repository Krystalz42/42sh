#include "libft.h"

char	*ft_strcat(char *dst, const char *src)
{
	void	*ptr;
	size_t	len;

	ptr = (void *)(dst + ft_strlen(dst));
	len = ft_strlen(src);
	ft_memcpy(ptr, (void *)src, len);
	((char *)ptr)[len] = 0;
	return (dst);
}
